"""Tests for the code preprocessor."""
import sys
from pathlib import Path
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from src.models import Language, SourceFile
from src.preprocessor import preprocess, _is_generated, _mostly_binary, _clean_source


def _sf(content: str, path: str, lang: Language = Language.JAVA) -> SourceFile:
    return SourceFile(
        path=f"/root/{path}", relative_path=path, language=lang,
        content=content, content_hash="abc123",
        size_bytes=len(content.encode()), line_count=content.count("\n") + 1,
    )


# ── Source file filtering ──

def test_java_source_kept():
    files = [_sf("public class A {}", "src/A.java", Language.JAVA)]
    result = preprocess(files)
    assert len(result.source_files) == 1

def test_python_source_kept():
    files = [_sf("class A: pass", "src/A.py", Language.PYTHON)]
